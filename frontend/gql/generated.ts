import gql from 'graphql-tag';
import * as Urql from 'urql';
export type Maybe<T> = T | null;
export type Exact<T extends { [key: string]: unknown }> = { [K in keyof T]: T[K] };
export type MakeOptional<T, K extends keyof T> = Omit<T, K> & { [SubKey in K]?: Maybe<T[SubKey]> };
export type MakeMaybe<T, K extends keyof T> = Omit<T, K> & { [SubKey in K]: Maybe<T[SubKey]> };
export type Omit<T, K extends keyof T> = Pick<T, Exclude<keyof T, K>>;
/** All built-in and custom scalars, mapped to their actual values */
export type Scalars = {
  ID: string;
  String: string;
  Boolean: boolean;
  Int: number;
  Float: number;
  Time: any;
};

export type Employee = {
  __typename?: 'Employee';
  id: Scalars['ID'];
  first_name: Scalars['String'];
  last_name: Scalars['String'];
  job_title: Scalars['String'];
  email: Scalars['String'];
  slack_id: Scalars['String'];
  slack_handle: Scalars['String'];
};

export type Mutation = {
  __typename?: 'Mutation';
  updateEmployee: Employee;
};


export type MutationUpdateEmployeeArgs = {
  input: UpdateEmployee;
};

export type Query = {
  __typename?: 'Query';
  employees: Array<Employee>;
  employee?: Maybe<Employee>;
  employeeByEmail?: Maybe<Employee>;
};


export type QueryEmployeeArgs = {
  id: Scalars['String'];
};


export type QueryEmployeeByEmailArgs = {
  email: Scalars['String'];
};

export type UpdateEmployee = {
  text: Scalars['String'];
};

export type EmployeesQueryVariables = Exact<{ [key: string]: never; }>;


export type EmployeesQuery = { __typename?: 'Query', employees: Array<{ __typename?: 'Employee', first_name: string, last_name: string }> };


export const EmployeesDocument = gql`
    query employees {
  employees {
    first_name
    last_name
  }
}
    `;

export function useEmployeesQuery(options: Omit<Urql.UseQueryArgs<EmployeesQueryVariables>, 'query'> = {}) {
  return Urql.useQuery<EmployeesQuery>({ query: EmployeesDocument, ...options });
};
